class MajorScales():
    def majorNum(self):
        if self.numNotes is None:
            self.numNotes = 7
        return self.numNotes


class PentatonicScales():
    def pentaNum(self):
        if self.numNotes is None:
            self.numNotes = 5
        return self.numNotes

